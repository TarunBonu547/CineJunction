package com.cinejunction.movie.enums;

/**
 * Represents the department or functional area a person works in
 * during movie production.
 */
public enum PersonDepartment {

    /**
     * On-camera performers who portray characters.
     */
    ACTING,

    /**
     * Individuals responsible for guiding the artistic and narrative aspects of the film.
     */
    DIRECTING,

    /**
     * Screenwriters and script contributors.
     */
    WRITING,

    /**
     * Producers and production staff managing the business and logistics.
     */
    PRODUCTION,

    /**
     * Composers, conductors, and music supervisors.
     */
    MUSIC,

    /**
     * Director of photography and camera crew.
     */
    CINEMATOGRAPHY,

    /**
     * Film editors and post-production editorial staff.
     */
    EDITING
}
