package com.cinejunction.movie.enums;

/**
 * Represents the specific role or contribution of a person
 * in relation to a movie.
 */
public enum CastRole {

    /**
     * A performer who portrays a character in the movie.
     */
    ACTOR,

    /**
     * The creative lead responsible for the overall vision and direction of the film.
     */
    DIRECTOR,

    /**
     * The author or authors of the screenplay.
     */
    WRITER,

    /**
     * An individual responsible for overseeing the production from a business or creative perspective.
     */
    PRODUCER,

    /**
     * A brief, often uncredited appearance, typically by a well-known person.
     */
    CAMEO
}
